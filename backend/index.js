  try {
    await mongoose.connect(process.env.MONGO_URL);
    console.log("âœ… Connected to MongoDB");
    console.log(`ğŸš€ Server running on http://localhost:${PORT}`);
  } catch (err) {
    console.error("âŒ DB Connection Error:", err);
  }
